import UIKit

extension UIColor {
    
    static let success: UIColor = {
        return UIColor(red: 0.0, green: 1.0, blue: 0.0, alpha: 0.25)
    }()

    static let failure: UIColor = {
        return UIColor(red: 1.0, green: 0.0, blue: 0.0, alpha: 0.25)
    }()

}
